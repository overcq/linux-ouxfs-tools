#!/bin/bash
#*******************************************************************************
if [ -n "$2" ]; then
    i="$1"
    end="$2"
else
    i=0
    if [ -n "$1" ]; then
        end="$1"
    else
        end=1000000
    fi
fi
while [ $i -ne $end ]; do
    ./1 $i || break
    i=$(( $i + 1 ))
done
